inline uint8_t CalculateCRC8(const uint8_t *data, size_t len) {
    uint8_t crc = 0x00;
    for (size_t i = 0; i < len; i++) {
        crc = pgm_read_byte(&kCRC8Table[crc ^ data[i]]);
    }
    return crc;
}

